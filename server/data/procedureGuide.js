/**
 * 특정 절차의 가이드를 반환
 *
 * @param {string} procedureCode - 절차 코드 (예: 'T-1-1')
 * @returns {Object|undefined}
 */
export function getGuideForProcedure(procedureCode) {
  return PROCEDURE_GUIDE[procedureCode]
}

/**
 * 특정 절차의 정합성 점검 대상 절차 코드 목록을 반환
 *
 * @param {string} procedureCode - 절차 코드
 * @returns {string[]} 점검 대상 절차 코드 배열 (비어있을 수 있음)
 */
export function getCoherenceTargets(procedureCode) {
  const guide = PROCEDURE_GUIDE[procedureCode]
  if (!guide?.coherenceCheck) return []
  return guide.coherenceCheck.checkAgainst || []
}

/**
 * 정합성 점검이 필요한 모든 절차를 반환
 *
 * @returns {Array<{code: string, checkAgainst: string[], description: string}>}
 */
export function getAllCoherenceChecks() {
  return Object.entries(PROCEDURE_GUIDE)
    .filter(([, guide]) => guide.coherenceCheck)
    .map(([code, guide]) => ({
      code,
      checkAgainst: guide.coherenceCheck.checkAgainst,
      description: guide.coherenceCheck.description,
    }))
}
